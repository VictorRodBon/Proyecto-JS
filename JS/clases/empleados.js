class Empleados {
    constructor(id, nombre, puesto, password, usuario, edad, salario) {
        this.id = id;
        this.nombre = nombre;
        this.puesto = puesto;
        this.password=password;
        this.usuario=usuario;
        this.edad = edad;
        this.salario = salario;
    }
}

export { Empleados };

